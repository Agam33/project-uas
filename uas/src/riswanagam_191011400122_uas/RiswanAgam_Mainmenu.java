    /*
        Animasi text 
    */
    private int dx = -60;
    public void play() {
        new Thread(() -> {
            while(true) {
                dx++;
                /*
                ketika posisi text sudah melebihi frame
                maka posisinya akan dimulai dari x = -180
                atau sebelum frame
                */
                if(dx > 780 ) dx = -180;
                try {
                    /*
                       delay waktu bergerak
                    */
                    Thread.sleep(15);
                } catch(Exception e) {e.printStackTrace();}
                /*
                    set lokasi pada text (label)
                */
                nama_aplikasi_label.setLocation(dx, 10);
            }
        }).start();
    }
