    /*
        Method yang digunakan untuk mengambil data seluruh
        uang kas pada database, dan dihitung totalnya
        lalu ditampilkan melalui variable total_uangKas.
    */
    private void totalUangKas() {
        try {
            ResultSet res = database.getResult(); 
            long totalUang = 0;
            PreparedStatement ps = database.getConn().prepareStatement("select SUM(uang_kas) as total_uang from penyimpanan");
            res = ps.executeQuery();
            if(res.next()) {
               // System.out.println(database.toString());
                totalUang = res.getLong("total_uang");
            }
            Locale locale = new Locale("id","ID");
            NumberFormat nf = NumberFormat.getCurrencyInstance(locale);
            total_uangKas.setText("Total Uang Kas "+nf.format(totalUang));
        } catch(SQLException e) {
            e.printStackTrace();
        }
    }
